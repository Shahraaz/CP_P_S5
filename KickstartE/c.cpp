bool isPrime(int n)
{
	if (n <= 1)
		return false;
	if (n == 2)
		return true;
	if (n % 2 == 0)
		return false;
	for (int i = 3; i * i <= n; i += 2)
		if (n % i == 0)
			return false;
	return true;
}

ll CCnt(int start, int end)
{
	int cnt = 0;
	for (; start <= end; ++start)
		cnt += isPrime(start);
	return cnt;
}

ll Count(int limit)
{
	int base = floor(sqrt(limit)) - step;
	if (base < 0)
	{
		return CCnt(0, limit);
	}
	else
	{
		base *= step;
		return M[base] + CCnt(base, limit);
	}
	return 0;
}

ll Solve(int num)
{
	if (num < 1)
		return 0;
	int ret = 1;
	ret += Count(num);
	db(num);
	db(ret);
	// ret += Count(num / 2) - 1;
	ret += max(Count(num / 2) - 1, 0LL);
	db(ret);
	ret += max(Count(num / 4) - 1, 0LL);
	ret += max(Count(num / 8) - 1, 0LL);
	db(ret);
	if (num >= 8)
		ret++;
	db(num, ret, Count(10));
	return ret;
}

