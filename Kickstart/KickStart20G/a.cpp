void solveCase()
{
    string s;
    cin >> s;
    ll res = 0;
    ll cnt = 0;
    for (int i = s.size() - 1; i >= 0; i--)
    {
        if (s.substr(i, 4) == "KICK")
            res += cnt;
        else if (s.substr(i, 5) == "START")
            cnt++;
    }
    cout << res << '\n';
}

