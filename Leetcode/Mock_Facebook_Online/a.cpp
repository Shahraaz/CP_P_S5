/**
 * // This is the interface that allows for creating nested lists.
 * // You should not implement it, or speculate about its implementation
 * class NestedInteger {
 *   public:
 *     // Constructor initializes an empty nested list.
 *     NestedInteger();
 *
 *     // Constructor initializes a single integer.
 *     NestedInteger(int value);
 *
 *     // Return true if this NestedInteger holds a single integer, rather than a nested list.
 *     bool isInteger() const;
 *
 *     // Return the single integer that this NestedInteger holds, if it holds a single integer
 *     // The result is undefined if this NestedInteger holds a nested list
 *     int getInteger() const;
 *
 *     // Set this NestedInteger to hold a single integer.
 *     void setInteger(int value);
 *
 *     // Set this NestedInteger to hold a nested list and adds a nested integer to it.
 *     void add(const NestedInteger &ni);
 *
 *     // Return the nested list that this NestedInteger holds, if it holds a nested list
 *     // The result is undefined if this NestedInteger holds a single integer
 *     const vector<NestedInteger> &getList() const;
 * };
 */
class Solution
{
    int result;
    int helper(vector<NestedInteger> nestedList)
    {
        int level = 1;
        for (auto elem : nestedList)
        {
            if (elem.isInteger())
                ;
            else
            {
                auto temporary = helper(elem.getList());
                level = max(level, temporary);
            }
        }
        return level + 1;
    }
    void fillArray(vector<NestedInteger> &nestedList, int level)
    {
        for (auto elem : nestedList)
            if (elem.isInteger())
                result += elem.getInteger() * level;
            else
                fillArray(elem.getList(),level-1);
    }

public:
    int depthSumInverse(vector<NestedInteger> &nestedList)
    {
        int level = helper(nestedList) - 1;
        result = 0;
        fillArray(nestedList, level);
        return result;
    }
};

